    private void sendMessageActionPerformed(java.awt.event.ActionEvent evt) {
        
        Message newMessage = new Message();
        newMessage.id = setNewID(messages);
        newMessage.sender = senderField.getText();
        newMessage.receiver = receiverField.getText();
        newMessage.message = jTextArea1.getText();
        
        try {
            messages.add(newMessage);
            JOptionPane.showMessageDialog(this, "Message Sent");
        } catch(Exception e) {
            System.out.println("error");
        }
        
        for(Message message : messages) {
            message.displayMessageConsole(message.id, message.sender, message.message, message.receiver);
            this.dispose();
        }
    
    }

    public void setPeople(String sender, String receiver) {
        senderField.setText(sender);
        receiverField.setText(receiver);
        senderField.setEditable(false);
        receiverField.setEditable(false);
    }
    
    public int setNewID(ArrayList<Message> messages) {
        int current = 0;
        for(int i = 0; i < messages.size(); i++) {
            current = messages.get(i).id;
        }
        return current + 1;
    }
    
    public void displayMessageConsole(int id, String sender, String message, String receiver) {
        System.out.println(this.id + " " + this.sender + " " + this.message + " " + this.receiver);
    }
    
